            <div id="section-1">
                <h4>Section 1: Title</h4>
                <p>This Act shall be known as the "Code of Conduct and Ethical Standards for Public Officials and
                    Employees."</p>
            </div>

            <div id="section-2">
                <h4>Section 2: Declaration of Policies</h4>
                <p>It is the policy of the State to promote a high standard of ethics in public service. Public
                    officials and employees shall at all times be accountable to the people and shall discharge their
                    duties with utmost responsibility, integrity, competence, and loyalty, act with patriotism and
                    justice, lead modest lives, and uphold public interest over personal interest.</p>
            </div>

            <div id="section-3">
                <h4>Section 3: Definition of Terms</h4>
                <p>As used in this Act, the term:</p>
                <ul>
                    <li><strong>(a) "Government"</strong> includes the national government, the local governments, and
                        all other instrumentalities, agencies or branches of the Republic of the Philippines including
                        government-owned or controlled corporations, and their subsidiaries.</li>
                    <li><strong>(b) "Public Officials"</strong> includes elective and appointive officials and
                        employees, permanent or temporary, whether in the career or non-career service, including
                        military and police personnel, whether or not they receive compensation, regardless of amount.
                    </li>
                    <li><strong>(c) "Gift"</strong> refers to a thing or a right disposed of gratuitously, or any act or
                        liberality, in favor of another who accepts it, and shall include a simulated sale or an
                        ostensibly onerous disposition thereof. It shall not include an unsolicited gift of nominal or
                        insignificant value not given in anticipation of, or in exchange for, a favor from a public
                        official or employee.</li>
                    <li><strong>(d) "Receiving any gift"</strong> includes the act of accepting directly or indirectly,
                        a gift from a person other than a member of his family or relative as defined in this Act, even
                        on the occasion of a family celebration or national festivity like Christmas, if the value of
                        the gift is neither nominal nor insignificant, or the gift is given in anticipation of, or in
                        exchange for, a favor.</li>
                    <li><strong>(e) "Loan"</strong> covers both simple loan and commodatum as well as guarantees,
                        financing arrangements or accommodations intended to ensure its approval.</li>
                    <li><strong>(f) "Substantial stockholder"</strong> means any person who owns, directly or
                        indirectly, shares of stock sufficient to elect a director of a corporation. This term shall
                        also apply to the parties to a voting trust.</li>
                    <li><strong>(g) "Family of public officials or employees"</strong> means their spouses and unmarried
                        children under eighteen (18) years of age.</li>
                    <li><strong>(h) "Person"</strong> includes natural and juridical persons unless the context
                        indicates otherwise.</li>
                    <li><strong>(i) "Conflict of interest"</strong> arises when a public official or employee is a
                        member of a board, an officer, or a substantial stockholder of a private corporation or owner or
                        has a substantial interest in a business, and the interest of such corporation or business, or
                        his rights or duties therein, may be opposed to or affected by the faithful performance of
                        official duty.</li>
                    <li><strong>(j) "Divestment"</strong> is the transfer of title or disposal of interest in property
                        by voluntarily, completely and actually depriving or dispossessing oneself of his right or title
                        to it in favor of a person or persons other than his spouse and relatives as defined in this
                        Act.</li>
                    <li><strong>(k) "Relatives"</strong> refers to any and all persons related to a public official or
                        employee within the fourth civil degree of consanguinity or affinity, including bilas, inso and
                        balae.</li>
                </ul>
            </div>

            <div id="section-4">
                <h4>Section 4: Norms of Conduct of Public Officials and Employees</h4>

                <p><strong>(A)</strong> Every public official and employee shall observe the following as standards of
                    personal conduct in the discharge and execution of official duties:</p>

                <ul>
                    <li><strong>(a) Commitment to public interest:</strong> Public officials and employees shall always
                        uphold the public interest over and above personal interest. All government resources and powers
                        of their respective offices must be employed and used efficiently, effectively, honestly, and
                        economically, particularly to avoid wastage in public funds and revenues.</li>

                    <li><strong>(b) Professionalism:</strong> Public officials and employees shall perform and discharge
                        their duties with the highest degree of excellence, professionalism, intelligence, and skill.
                        They shall enter public service with utmost devotion and dedication to duty. They shall endeavor
                        to discourage wrong perceptions of their roles as dispensers or peddlers of undue patronage.
                    </li>

                    <li><strong>(c) Justness and sincerity:</strong> Public officials and employees shall remain true to
                        the people at all times. They must act with justness and sincerity and shall not discriminate
                        against anyone, especially the poor and the underprivileged. They shall at all times respect the
                        rights of others and refrain from acts contrary to law, good morals, good customs, public
                        policy, public order, public safety, and public interest. They shall not dispense or extend
                        undue favors to their relatives except with respect to appointments considered strictly
                        confidential or as members of their personal staff whose terms are coterminous with theirs.</li>

                    <li><strong>(d) Political neutrality:</strong> Public officials and employees shall provide service
                        to everyone without unfair discrimination and regardless of party affiliation or preference.
                    </li>

                    <li><strong>(e) Responsiveness to the public:</strong> Public officials and employees shall extend
                        prompt, courteous, and adequate service to the public. They shall ensure openness of
                        information, encourage suggestions, avoid red tape, and develop an understanding of the
                        socio-economic conditions, especially in the rural and urban areas.</li>

                    <li><strong>(f) Nationalism and patriotism:</strong> Public officials and employees shall promote
                        the use of locally produced goods and technology and encourage appreciation of the country. They
                        shall maintain and defend Philippine sovereignty against foreign intrusion.</li>

                    <li><strong>(g) Commitment to democracy:</strong> Public officials and employees shall commit to the
                        democratic way of life, maintain the principle of public accountability, and uphold the
                        Constitution. They shall put loyalty to country above loyalty to persons or party.</li>

                    <li><strong>(h) Simple living:</strong> Public officials and employees and their families shall lead
                        modest lives appropriate to their positions and income. They shall not indulge in extravagant or
                        ostentatious display of wealth in any form.</li>
                </ul>

                <p><strong>(B)</strong> The Civil Service Commission shall adopt positive measures to promote observance
                    of these standards, including the dissemination of information programs, workshops, merit increases,
                    and continuing research to raise the general level of observance.</p>
            </div>

            <div id="section-5">
                <h4>Section 5: Duties of Public Officials and Employees</h4>

                <p>In the performance of their duties, all public officials and employees are under obligation to:</p>

                <ul>
                    <li><strong>(a) Act promptly on letters and requests:</strong> All public officials and employees
                        shall, within fifteen (15) working days from receipt thereof, respond to letters, telegrams, or
                        other means of communications sent by the public. The reply must contain the action taken on the
                        request.</li>

                    <li><strong>(b) Submit annual performance reports:</strong> All heads or other responsible officers
                        of offices and agencies of the government and of government-owned or controlled corporations
                        shall, within forty-five (45) working days from the end of the year, render a performance report
                        of the agency or office or corporation concerned. Such report shall be open and available to the
                        public within regular office hours.</li>

                    <li><strong>(c) Process documents and papers expeditiously:</strong> All official papers and
                        documents must be processed and completed within a reasonable time from the preparation thereof
                        and must contain, as far as practicable, not more than three (3) signatories therein. In the
                        absence of duly authorized signatories, the official next-in-rank or officer-in-charge shall
                        sign for and in their behalf.</li>

                    <li><strong>(d) Act immediately on the public's personal transactions:</strong> All public officials
                        and employees must attend to anyone who wants to avail themselves of the services of their
                        offices and must, at all times, act promptly and expeditiously.</li>

                    <li><strong>(e) Make documents accessible to the public:</strong> All public documents must be made
                        accessible to, and readily available for inspection by, the public within reasonable working
                        hours.</li>
                </ul>
            </div>

            <div id="section-6">
                <h4>Section 6: System of Incentives and Rewards</h4>

                <p>A system of annual incentives and rewards is hereby established in order to motivate and inspire
                    public servants to uphold the highest standards of ethics. For this purpose, a Committee on Awards
                    to Outstanding Public Officials and Employees is hereby created composed of the following: the
                    Ombudsman and Chairman of the Civil Service Commission as Co-Chairmen, and the Chairman of the
                    Commission on Audit, and two government employees to be appointed by the President, as members.</p>

                <p>It shall be the task of this Committee to conduct a periodic, continuing review of the performance of
                    public officials and employees, in all the branches and agencies of Government and establish a
                    system of annual incentives and rewards to the end that due recognition is given to public officials
                    and employees of outstanding merit on the basis of the standards set forth in this Act.</p>

                <p>The conferment of awards shall take into account, among other things, the following: the years of
                    service and the quality and consistency of performance, the obscurity of the position, the level of
                    salary, the unique and exemplary quality of a certain achievement, and the risks or temptations
                    inherent in the work. Incentives and rewards to government officials and employees of the year to be
                    announced in public ceremonies honoring them may take the form of bonuses, citations, directorships
                    in government-owned or controlled corporations, local and foreign scholarship grants, paid vacations
                    and the like.</p>

                <p>They shall likewise be automatically promoted to the next higher position with the commensurate
                    salary suitable to their qualifications. In case there is no next higher position or it is not
                    vacant, said position shall be included in the budget of the office in the next General
                    Appropriations Act. The Committee on Awards shall adopt its own rules to govern the conduct of its
                    activities.</p>
            </div>

            <div id="section-7">
                <h4>Section 7: Prohibited Acts and Transactions</h4>

                <p>In addition to acts and omissions of public officials and employees now prescribed in the
                    Constitution and existing laws, the following shall constitute prohibited acts and transactions of
                    any public official and employee and are hereby declared to be unlawful:</p>

                <ul>
                    <li><strong>(a) Financial and material interest:</strong> Public officials and employees shall not,
                        directly or indirectly, have any financial or material interest in any transaction requiring the
                        approval of their office.</li>

                    <li><strong>(b) Outside employment and other activities related thereto:</strong> Public officials
                        and employees during their incumbency shall not:
                        <ul>
                            <li><strong>(1)</strong> Own, control, manage or accept employment as officer, employee,
                                consultant, counsel, broker, agent, trustee or nominee in any private enterprise
                                regulated, supervised or licensed by their office unless expressly allowed by law;</li>
                            <li><strong>(2)</strong> Engage in the private practice of their profession unless
                                authorized by the Constitution or law, provided that such practice will not conflict or
                                tend to conflict with their official functions;</li>
                            <li><strong>(3)</strong> Recommend any person to any position in a private enterprise which
                                has a regular or pending official transaction with their office.</li>
                        </ul>
                        These prohibitions shall continue to apply for a period of one (1) year after resignation,
                        retirement, or separation from public office, except in the case of subparagraph (b)(2) above.
                        However, the professional concerned cannot practice his profession in connection with any matter
                        before the office he used to be with, in which case the one-year prohibition shall likewise
                        apply.
                    </li>

                    <li><strong>(c) Disclosure and/or misuse of confidential information:</strong> Public officials and
                        employees shall not use or divulge confidential or classified information officially known to
                        them by reason of their office and not made available to the public, either:
                        <ul>
                            <li><strong>(1)</strong> To further their private interests, or give undue advantage to
                                anyone;</li>
                            <li><strong>(2)</strong> To prejudice the public interest.</li>
                        </ul>
                    </li>

                    <li><strong>(d) Solicitation or acceptance of gifts:</strong> Public officials and employees shall
                        not solicit or accept, directly or indirectly, any gift, gratuity, favor, entertainment, loan,
                        or anything of monetary value from any person in the course of their official duties or in
                        connection with any operation being regulated by, or any transaction which may be affected by
                        the functions of their office. As to gifts or grants from foreign governments, the Congress
                        consents to:
                        <ul>
                            <li><strong>(i)</strong> The acceptance and retention by a public official or employee of a
                                gift of nominal value tendered and received as a souvenir or mark of courtesy;</li>
                            <li><strong>(ii)</strong> The acceptance by a public official or employee of a gift in the
                                nature of a scholarship or fellowship grant or medical treatment;</li>
                            <li><strong>(iii)</strong> The acceptance by a public official or employee of travel grants
                                or expenses for travel taking place entirely outside the Philippines (such as
                                allowances, transportation, food, and lodging) of more than nominal value if such
                                acceptance is appropriate or consistent with the interests of the Philippines, and
                                permitted by the head of office, branch, or agency to which he belongs.</li>
                        </ul>
                        The Ombudsman shall prescribe such regulations as may be necessary to carry out the purpose of
                        this subsection, including pertinent reporting and disclosure requirements. Nothing in this Act
                        shall be construed to restrict or prohibit any educational, scientific, or cultural exchange
                        programs subject to national security requirements.
                    </li>
                </ul>
            </div>

            <div id="section-8">
                <h4>Section 8: Statements and Disclosure</h4>

                <p>Public officials and employees have an obligation to accomplish and submit declarations under oath
                    of, and the public has the right to know, their assets, liabilities, net worth, and financial and
                    business interests, including those of their spouses and of unmarried children under eighteen (18)
                    years of age living in their households.</p>

                <h5>(A) Statements of Assets and Liabilities and Financial Disclosure</h5>
                <p>All public officials and employees, except those who serve in an honorary capacity, laborers, and
                    casual or temporary workers, shall file under oath their Statement of Assets, Liabilities and Net
                    Worth and a Disclosure of Business Interests and Financial Connections, and those of their spouses
                    and unmarried children under eighteen (18) years of age living in their households.</p>

                <p>The two documents shall contain information on the following:</p>
                <ul>
                    <li><strong>(a)</strong> Real property, its improvements, acquisition costs, assessed value, and
                        current fair market value;</li>
                    <li><strong>(b)</strong> Personal property and acquisition cost;</li>
                    <li><strong>(c)</strong> All other assets such as investments, cash on hand or in banks, stocks,
                        bonds, and the like;</li>
                    <li><strong>(d)</strong> Liabilities;</li>
                    <li><strong>(e)</strong> All business interests and financial connections.</li>
                </ul>

                <p>The documents must be filed:</p>
                <ul>
                    <li><strong>(a)</strong> Within thirty (30) days after assumption of office;</li>
                    <li><strong>(b)</strong> On or before April 30, of every year thereafter;</li>
                    <li><strong>(c)</strong> Within thirty (30) days after separation from the service.</li>
                </ul>

                <p>All public officials and employees required under this section to file the aforementioned documents
                    shall also execute, within thirty (30) days from the date of their assumption of office, the
                    necessary authority in favor of the Ombudsman to obtain from all appropriate government agencies,
                    including the Bureau of Internal Revenue, such documents as may show their assets, liabilities, net
                    worth, and also their business interests and financial connections in previous years, including, if
                    possible, the year when they first assumed any office in the Government.</p>

                <p>Husband and wife who are both public officials or employees may file the required statements jointly
                    or separately.</p>

                <h5>The Statements of Assets, Liabilities and Net Worth and the Disclosure of Business Interests and
                    Financial Connections shall be filed by:</h5>
                <ul>
                    <li><strong>(1)</strong> Constitutional and national elective officials, with the national office of
                        the Ombudsman;</li>
                    <li><strong>(2)</strong> Senators and Congressmen, with the Secretaries of the Senate and the House
                        of Representatives, respectively; Justices, with the Clerk of Court of the Supreme Court;
                        Judges, with the Court Administrator; and all national executive officials with the Office of
                        the President;</li>
                    <li><strong>(3)</strong> Regional and local officials and employees, with the Deputy Ombudsman in
                        their respective regions;</li>
                    <li><strong>(4)</strong> Officers of the armed forces from the rank of colonel or naval captain,
                        with the Office of the President, and those below said ranks, with the Deputy Ombudsman in their
                        respective regions;</li>
                    <li><strong>(5)</strong> All other public officials and employees, defined in Republic Act No. 3019,
                        as amended, with the Civil Service Commission.</li>
                </ul>

                <h5>(B) Identification and Disclosure of Relatives</h5>
                <p>It shall be the duty of every public official or employee to identify and disclose, to the best of
                    his knowledge and information, his relatives in the Government in the form, manner, and frequency
                    prescribed by the Civil Service Commission.</p>

                <h5>(C) Accessibility of Documents</h5>
                <ul>
                    <li><strong>(1)</strong> Any and all statements filed under this Act shall be made available for
                        inspection at reasonable hours.</li>
                    <li><strong>(2)</strong> Such statements shall be made available for copying or reproduction after
                        ten (10) working days from the time they are filed as required by law.</li>
                    <li><strong>(3)</strong> Any person requesting a copy of a statement shall be required to pay a
                        reasonable fee to cover the cost of reproduction and mailing of such statement, as well as the
                        cost of certification.</li>
                    <li><strong>(4)</strong> Any statement filed under this Act shall be available to the public for a
                        period of ten (10) years after receipt of the statement. After such period, the statement may be
                        destroyed unless needed in an ongoing investigation.</li>
                </ul>

                <h5>(D) Prohibited Acts</h5>
                <p>It shall be unlawful for any person to obtain or use any statement filed under this Act for:</p>
                <ul>
                    <li><strong>(a)</strong> Any purpose contrary to morals or public policy; or</li>
                    <li><strong>(b)</strong> Any commercial purpose other than by news and communications media for
                        dissemination to the general public.</li>
                </ul>
            </div>

            <div id="section-9">
                <h4>Section 9: Divestment</h4>

                <p>A public official or employee shall avoid conflicts of interest at all times. When a conflict of
                    interest arises, he shall resign from his position in any private business enterprise within thirty
                    (30) days from his assumption of office and/or divest himself of his shareholdings or interest
                    within sixty (60) days from such assumption.</p>

                <p>The same rule shall apply where the public official or employee is a partner in a partnership.</p>

                <p>The requirement of divestment shall not apply to those who serve the Government in an honorary
                    capacity nor to laborers and casual or temporary workers.</p>
            </div>

            <div id="section-10">
                <h4>Section 10: Review and Compliance Procedure</h4>

                <h5>(a) Review of Statements</h5>
                <p>The designated Committees of both Houses of Congress shall establish procedures for the review of
                    statements to determine whether said statements, which have been submitted on time, are complete,
                    and are in proper form. In the event a determination is made that a statement is not so filed, the
                    appropriate Committee shall so inform the reporting individual and direct him to take the necessary
                    corrective action.</p>

                <h5>(b) Power to Render Opinions</h5>
                <p>In order to carry out their responsibilities under this Act, the designated Committees of both Houses
                    of Congress shall have the power within their respective jurisdictions, to render any opinion
                    interpreting this Act, in writing, to persons covered by this Act, subject in each instance to the
                    approval by affirmative vote of the majority of the particular House concerned.</p>

                <p>The individual to whom an opinion is rendered, and any other individual involved in a similar factual
                    situation, and who, after issuance of the opinion, acts in good faith in accordance with it shall
                    not be subject to any sanction provided in this Act.</p>

                <h5>(c) Duties of Heads of Offices</h5>
                <p>The heads of other offices shall perform the duties stated in subsections (a) and (b) hereof insofar
                    as their respective offices are concerned, subject to the approval of the Secretary of Justice, in
                    the case of the Executive Department and the Chief Justice of the Supreme Court, in the case of the
                    Judicial Department.</p>
            </div>

            <div id="section-11">
                <h4>Section 11: Penalties</h4>

                <p><strong>(a)</strong> Any public official or employee, regardless of whether or not he holds office or
                    employment in a casual, temporary, holdover, permanent, or regular capacity, committing any
                    violation of this Act shall be punished with a fine not exceeding the equivalent of six (6) months'
                    salary or suspension not exceeding one (1) year, or removal, depending on the gravity of the offense
                    after due notice and hearing by the appropriate body or agency. If the violation is punishable by a
                    heavier penalty under another law, he shall be prosecuted under the latter statute. Violations of
                    Sections 7, 8, or 9 of this Act shall be punishable with imprisonment not exceeding five (5) years,
                    or a fine not exceeding five thousand pesos (P5,000), or both, and, in the discretion of the court
                    of competent jurisdiction, disqualification to hold public office.</p>

                <p><strong>(b)</strong> Any violation hereof proven in a proper administrative proceeding shall be
                    sufficient cause for removal or dismissal of a public official or employee, even if no criminal
                    prosecution is instituted against him.</p>

                <p><strong>(c)</strong> Private individuals who participate in conspiracy as co-principals, accomplices,
                    or accessories, with public officials or employees, in violation of this Act, shall be subject to
                    the same penal liabilities as the public officials or employees and shall be tried jointly with
                    them.</p>

                <p><strong>(d)</strong> The official or employee concerned may bring an action against any person who
                    obtains or uses a report for any purpose prohibited by Section 8 (D) of this Act. The Court in which
                    such action is brought may assess against such person a penalty in any amount not to exceed
                    twenty-five thousand pesos (P25,000.00). If another sanction hereunder or under any other law is
                    heavier, the latter shall apply.</p>
            </div>

            <div id="section-12">
                <h4>Section 12: Promulgation of Rules and Regulations, Administration and Enforcement of this Act</h4>

                <p>The Civil Service Commission shall have the primary responsibility for the administration and
                    enforcement of this Act. It shall transmit all cases for prosecution arising from violations of this
                    Act to the proper authorities for appropriate action: Provided, however, That it may institute such
                    administrative actions and disciplinary measures as may be warranted in accordance with law. Nothing
                    in this provision shall be construed as a deprivation of the right of each House of Congress to
                    discipline its Members for disorderly behavior.</p>

                <p>The Civil Service Commission is hereby authorized to promulgate rules and regulations necessary to
                    carry out the provisions of this Act, including guidelines for individuals who render free voluntary
                    service to the Government. The Ombudsman shall likewise take steps to protect citizens who denounce
                    acts or omissions of public officials and employees which are in violation of this Act.</p>
            </div>

            <div id="section-13">
                <h4>Section 13: Provisions for More Stringent Standards</h4>

                <p>Nothing in this Act shall be construed to derogate from any law, or any regulation prescribed by any
                    body or agency, which provides for more stringent standards for its officials and employees.</p>
            </div>

            <div id="section-14">
                <h4>Section 14: Appropriations</h4>

                <p>The sum necessary for the effective implementation of this Act shall be taken from the appropriations
                    of the Civil Service Commission. Thereafter, such sum as may be needed for its continued
                    implementation shall be included in the Annual General Appropriations Act.</p>
            </div>

            <div id="section-15">
                <h4>Section 15: Separability Clause</h4>

                <p>If any provision of this Act or the application of such provision to any person or circumstance is
                    declared invalid, the remainder of the Act or the application of such provision to other persons or
                    circumstances shall not be affected by such declaration.</p>
            </div>

            <div id="section-16">
                <h4>Section 16: Repealing Clause</h4>

                <p>All laws, decrees, and orders or parts thereof inconsistent herewith, are deemed repealed or modified
                    accordingly, unless the same provide for a heavier penalty.</p>
            </div>

            <div id="section-17">
                <h4>Section 17: Effectivity</h4>

                <p>This Act shall take effect after thirty (30) days following the completion of its publication in the
                    Official Gazette or in two (2) national newspapers of general circulation.</p>
            </div>
